    HashSet<String> set = new HashSet<String>();

    public boolean wordBreak(String s, List<String> wordDict) {

        for (int index = 1; index < s.length() + 1; index++) {

            String temp = s.substring(0, index);

            if (wordDict.contains(temp)) {
                // 获取下一次递归的字符串
                String nextS = s.substring(index);

                // 检测字符串是否为空，若为空，说明全部递归已完成，返回true
                if (nextS.isEmpty())
                    return true;

                // 检测此字符串之前是否尝试过，若尝试过，则直接跳过
                if (set.contains(nextS))
                    continue;

                // 如果是第一次检测此字符串，将字符串推入set中记录
                set.add(nextS);

                if (wordBreak(nextS, wordDict))
                    return true;
            }
        }

        return false;
    }

    // 广度优先算法
    public boolean wordBreak2(String s, List<String> wordDict) {
        Queue<String> queue = new LinkedList<String>();

        for (int i = 0; i < s.length() + 1; i++) {
            String temp = s.substring(0, i);
            if (wordDict.contains(temp)) {
                String newStr = s.substring(i);
                if(newStr.isEmpty())
                    return true;
                set.add(newStr);
                queue.add(newStr);
            }
        }

        while (!queue.isEmpty()) {
            int nextLevelLength = queue.size();
            for (int i = 0; i < nextLevelLength; i++) {
                String str = queue.poll();

                for (int j = 0; j < str.length() + 1; j++) {
                    String temp = str.substring(0, j);

                    if (wordDict.contains(temp)) {
                        String newStr = str.substring(j);
                        if(newStr.isEmpty())
                            return true;
                        if(set.contains(newStr)) 
                            continue;
                        
                            set.add(newStr);
                        queue.add(str.substring(j));
                    }
                }
            }
        }

        return false;
    }


